SELECT COUNT(1)
FROM vw_bill
WHERE bill_id = :bill_id
  AND bill_nbr = :bill_nbr
  AND party_id = :party_id
  AND bill_sub_acct_id = :bill_sub_acct_id
  AND tariff_type = :tariff_type
  AND template_type = :template_type
  AND lcp = :lcp
  AND acct_type = :acct_type
  AND account_id = :account_id
  AND business_unit = :business_unit
  AND bill_dt = :bill_dt
  AND bill_cyc_id = :bill_cyc_id
  AND start_dt = :start_dt
  AND end_dt = :end_dt
  AND currency_cd = :currency_cd
  AND bill_amt = :bill_amt
  AND bill_ref = :bill_ref
  AND status = :status
  AND adhoc_bill_flg = :adhoc_bill_flg
  AND sett_sub_lvl_type = :sett_sub_lvl_type
  AND sett_sub_lvl_val = :sett_sub_lvl_val
  AND granularity = :granularity
  AND granularity_key_val = :granularity_key_val
  AND rel_waf_flg = :rel_waf_flg
  AND rel_reserve_flg = :rel_reserve_flg
  AND fastest_pay_route = :fastest_pay_route
  AND case_id = :case_id
  AND individual_bill = :individual_bill
  AND manual_narrative = :manual_narrative
  AND settlement_region_id = :settlement_region_id
  AND prev_bill_id = :prev_bill_id
  AND debt_dt = :debt_dt
  AND debt_mig_type = :debt_mig_type
  AND merch_tax_reg = :merch_tax_reg
  AND wp_tax_reg = :wp_tax_reg
  AND tax_type = :tax_type
  AND tax_authority = :tax_authority
