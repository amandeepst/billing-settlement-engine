	BILLABLE_CHARGE_ID CHAR(12),
	LINE_SEQUENCE NUMBER(3,0),
	CHARGE_AMOUNT NUMBER(15,2),
	CURRENCY_CD CHAR(3),
	DISTRIBUTION_ID CHAR(10),
	PRECISE_CHARGE_AMOUNT NUMBER(36,18),
