	BATCH_ATTEMPT NUMBER(3,0) NOT NULL ,
	PARTITION_ID NUMBER(*,0) NOT NULL ,
	ILM_DT TIMESTAMP (6) NOT NULL ,
	ILM_ARCH_SW CHAR(1) NOT NULL ,
	DESCRIPTION_ON_BILL VARCHAR2(80),
	CHARACTERISTIC_VALUE CHAR(16),
	PARTITION NUMBER(5,0) GENERATED ALWAYS AS (ORA_HASH(BILLABLE_ITEM_ID,127)) VIRTUAL ,
